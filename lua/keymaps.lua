-- [[ Основные привязки клавиш ]]
--  См. `:help vim.keymap.set()`

-- Очистка подсветки поиска при нажатии <Esc> в нормальном режиме
--  См. `:help hlsearch`
vim.keymap.set('n', '<Esc>', '<cmd>nohlsearch<CR>')

-- Привязки клавиш для диагностики
vim.keymap.set('n', '<leader>q', vim.diagnostic.setloclist, { desc = 'Открыть список диагностик [Q]uickfix' })

-- Выход из терминального режима во встроенном терминале с использованием
-- более простого сочетания клавиш для удобства. В противном случае обычно
-- нужно нажимать <C-\><C-n>, что сложно угадать без опыта.
--
-- ПРИМЕЧАНИЕ: Это может не работать во всех терминальных эмуляторах/tmux/и т.д.
-- Попробуйте свою привязку или просто используйте <C-\><C-n> для выхода из терминального режима
vim.keymap.set('t', '<Esc><Esc>', '<C-\\><C-n>', { desc = 'Выход из терминального режима' })

