-- Привязки клавиш для упрощения навигации по сплитам.
--  Используйте CTRL+<hjkl> для переключения между окнами
--
--  См. `:help wincmd` для списка всех команд окон
vim.keymap.set('n', '<C-h>', '<C-w><C-h>', { desc = 'Переместить фокус в левое окно' })
vim.keymap.set('n', '<C-l>', '<C-w><C-l>', { desc = 'Переместить фокус в правое окно' })
vim.keymap.set('n', '<C-j>', '<C-w><C-j>', { desc = 'Переместить фокус в нижнее окно' })
vim.keymap.set('n', '<C-k>', '<C-w><C-k>', { desc = 'Переместить фокус в верхнее окно' })

